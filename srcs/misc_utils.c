#include "../push_swap.h"

/* -------------------------------------------------------------------------- */

void	reset_stack(t_stack *stack)
{
	if (!stack)
		return ;
	stack->head = NULL;
	stack->size = 0;
	stack->tail = NULL;
}

/* -------------------------------------------------------------------------- */

void	patch_stack(t_stack *stack)
{
	if (!stack || stack->size == 0)
		return ;
	if (stack->head && stack->tail)
	{
		stack->head->prev = stack->tail;
		stack->tail->next = stack->head;
	}
}

